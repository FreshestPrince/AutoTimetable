def time_translate(number):
    if number < 13:
        return "Monday" + "-" + str(time_normal(number))
    elif 26 > number:
        return "Tuesday" + "-" + str(time_normal(number))
    elif 39 > number:
        return "Wednesday" + "-" + str(time_normal(number))
    elif 52 > number:
        return "Thursday" + "-" + str(time_normal(number))
    elif 65 > number:
        return "Friday" + "-" + str(time_normal(number))
    else:
        print("Value is not within proper range")
        return 0


